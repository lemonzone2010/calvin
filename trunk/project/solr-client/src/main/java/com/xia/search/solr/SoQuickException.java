package com.xia.search.solr;

public class SoQuickException extends RuntimeException {
	private static final long serialVersionUID = 1L;

	public SoQuickException() {
		super();
	}

	public SoQuickException(String message, Throwable cause) {
		super(message, cause);
	}

	public SoQuickException(String message) {
		super(message);
	}

	public SoQuickException(Throwable cause) {
		super(cause);
	}

}
